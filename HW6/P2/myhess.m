function H = myhess(X)
    x1 = X(1);
    x2 = X(2);
    A = -0.5*(x1*x2 - 0.75)^2 - 2*(x1 - 1.5)^2;
    B = 1/(x1 + 1/3) - x2 - 0.5;
    expA = exp(A);
    global rho;
    H11 = expA * (-x2*(x1*x2 - 0.75) - 4*(x1 - 1.5)) * (x2*(x1*x2 - 0.75) + 4*(x1 - 1.5)) + expA * (x2^2 + 4) ...
                    + (2*rho)/(x1 + 1/3)^4 + (4*rho*B)/(x1 + 1/3)^3;
    H12 = expA * (-x1*(x1*x2 - 0.75)) * (x2*(x1*x2 - 0.75) + 4*(x1 - 1.5)) * expA * (2*x1*(x1*x2 - 0.75)) ...
                    + (2*rho)/(x1 + 1/3)^2;
    H22 = expA * x1^2 * (1 - (x1*x2 - 0.75)^2) + 2*rho;
    H = [H11, H12; H12, H22];